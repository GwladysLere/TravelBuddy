		<h2>Durant le Trajet</h2>
		
		<h3>Conducteurs - 10 conseils pour un trajet serein.</h3>

		<ol>
			<li><h4>Partez reposé et prenez des pauses</h4>
			<p>La fatigue est la 1ère cause d’accidents mortels sur autoroute et pourtant 65 % des 18/24 ans et 50 % de l’ensemble des conducteurs prennent le volant alors qu’ils sont fatigués. A 90 km/h, on 
			parcourt 25 mètres par seconde, et à 130, km/h, 36m par seconde ! Alors soyez vigilants et prenez des pauses de 15 à 20 minutes toutes les deux heures.</p></li>
			<li><h4>Echangez avec vos covoitureurs</h4>
			<p>Cela réduira fortement le risque de somnolence.</p></li>
			<li><h4>Ceinture de sécurité</h4>
			<p>Elle est obligatoire dans les véhicules légers à l'avant, comme à l'arrière. Un passager = une place = une ceinture. </p></li>
			<li><h4>Bébé à bord</h4>
			<p>Les bébés doivent voyager dans un siège auto adapté à leur âge et taille. Le siège doit être impérativement homologué et doit être installé dos à la route, idéalement à l’arrière. </p></li>
			<li><h4>Distances de sécurité </h4>
			<p>Sur autoroute, à 130 km/h, vous devez au moins laisser un intervalle de 2 traits, soit environ 90 mètres pour arrêter votre véhicule sans percuter celui qui vous précède. En cas d’intempéries, doublez les distances pour anticiper toute défaillance des autres conducteurs. </p></li>
			<li><h4>Téléphone au volant </h4>
			<p>Téléphoner au volant multiplie par 3 le risque d’accident. Lorsqu'on lit ou écrit un SMS on se prive d'une main, de la vue et de l'attention. C’est un peu comme conduire les yeux fermés… difficile alors de se concentrer et d'anticiper un danger. Rappel : depuis le 1er juillet 2015, l'usage d'écouteurs, oreillettes ou casques est interdit aux conducteurs. Cette infraction est passible d'une amende de 135 € et d'un retrait de 3 points du permis de conduire. </p></li>
			<li><h4>Alcool et stupéfiants</h4>
			<p>A partir de 0,5 g/l dans le sang ou 0,25 mg/l dans l’air expiré, la conduite est interdite. A 0,5 g/l le risque d’accident est multiplié par 2. Un tel taux correspond en moyenne à de 2 verres de vin (10 cl) ou de bière (25 cl). Pour les conducteurs novices, le taux autorisé est de 0,2 g/l. Concrètement, c'est zéro verre d'alcool si vous avez le permis depuis moins de 3 ans. Stupéfiants = retrait immédiat du permis pour mise en danger de la vie d'autrui. Le cannabis altère la vision, l’audition et les capacités de coordination. Par ailleurs, les effets du cannabis et de l'alcool se cumulent. Un conducteur positif à la fois au cannabis et à l'alcool a 14 fois plus de risque d'être responsable d'un accident mortel. </p></li>
			<li><h4>Conduite la nuit </h4>
			<p>Elle occasionne une fatigue visuelle et nerveuse, d’autant plus s’il pleut. Une pause est recommandée toutes les heures. </p></li>
			<li><h4>Conduite sous la pluie</h4>
			<p> Mauvaise visibilité, perte d'adhérence, aquaplaning… les distances de freinage augmentent de 30 % sur sol mouillé. Pensez à réduire votre vitesse, augmenter les distances de sécurité et allumer les feux de croisement. </p></li>
			<li><h4>Conduite sur neige </h4>
			<p> Roulez lentement, voire très lentement, sans à-coup, comme si votre véhicule n’avait pas de freins. Si vous souhaitez vous arrêter, privilégiez le frein moteur puis freinez doucement. En descente, afin de limiter la prise de vitesse, utilisez également le frein moteur pour ralentir. À l’abord de virages, réduisez votre vitesse ; et une fois dans le virage, maintenez-la pour limiter la perte d’adhérence. </p></li>
		</ol> 
		
